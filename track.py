#!/usr/bin/env python3
"""WorkQuarry tracker helper. Issue frontmatter in <tracker>/issues/*.md is the
database; BACKLOG.md is a generated index; DONE.md is an append-only event log.

Areas and paths are NOT hardcoded: they come from tracker/config.json, discovered
by walking up from the current directory (falling back to this script's repo root).
That is what lets one copy of this file serve any repo.

Usage:
  python tools/track.py new --title "..." --area <area> [--type idea] [--priority ?]
                            [--effort ?] [--risk ?] [--status idea] [--sprint NAME]
                            [--links "a, b"] [--dry-run]
  python tools/track.py set ID [--status ready] [--priority p1] [--effort M]
                            [--risk low] [--sprint NAME|none] [--title "..."]
  python tools/track.py list [--status S] [--area A] [--type T] [--priority P]
                            [--sprint NAME] [--open] [--sort priority|id|status|sprint]
                            [--format table|csv]
  python tools/track.py close ID [--outcome "done (abc1234)"] [--dropped]
  python tools/track.py report [--html PATH] [--md PATH]  # shareable snapshot
  python tools/track.py index      # regenerate BACKLOG.md from frontmatter
  python tools/track.py next-id    # print next available issue number

new/set/close regenerate BACKLOG.md automatically. Never hand-edit BACKLOG.md.
report renders a point-in-time snapshot (Markdown to stdout with no path);
it never writes into the tracker's own data.
"""
import argparse, csv, datetime, io, json, re, sys
from pathlib import Path

FIELDS = ["id", "title", "type", "status", "priority", "effort", "risk",
          "area", "sprint", "created", "closed", "links"]
PRI_ORDER = {"p1": 1, "p2": 2, "p3": 3, "?": 4}
STATUS_ORDER = {"in-progress": 1, "ready": 2, "idea": 3, "done": 8, "dropped": 9}


def find_config() -> Path:
    """Locate tracker/config.json: search cwd upward, then this script's repo root."""
    here = Path.cwd()
    for base in (here, *here.parents):
        cfg = base / "tracker" / "config.json"
        if cfg.exists():
            return cfg
    cfg = Path(__file__).resolve().parent.parent / "tracker" / "config.json"
    if cfg.exists():
        return cfg
    sys.exit("workquarry: no tracker/config.json found. Run the installer or create one "
             "(see tracker/config.example.json).")


CONFIG_PATH = find_config()
CONFIG = json.loads(CONFIG_PATH.read_text(encoding="utf-8"))
ROOT = CONFIG_PATH.parent.parent
_paths = CONFIG.get("paths", {})
ISSUES = ROOT / _paths.get("issues", "tracker/issues")
BACKLOG = ROOT / _paths.get("backlog", "tracker/BACKLOG.md")
DONE = ROOT / _paths.get("done", "tracker/DONE.md")
AREAS = set(CONFIG.get("areas", []))


def today() -> str:
    return datetime.date.today().isoformat()


def next_num() -> int:
    nums = [int(m.group(1)) for f in ISSUES.glob("*-[0-9]*.md")
            if (m := re.search(r"-(\d+)\.md$", f.name))]
    return max(nums, default=0) + 1


def read_text(path: Path) -> str:
    try:
        return path.read_text(encoding="utf-8")
    except UnicodeDecodeError:  # some issue files arrive cp1252-encoded
        return path.read_text(encoding="cp1252")


def parse_frontmatter(path: Path) -> dict:
    text = read_text(path)
    m = re.match(r"^---\n(.*?)\n---", text, re.DOTALL)
    fm = {"_path": path}
    if not m:
        return fm
    for line in m.group(1).splitlines():
        if km := re.match(r"^(\w[\w-]*):\s*(.*)$", line):
            fm[km.group(1)] = km.group(2).strip().strip('"')
    return fm


def all_issues() -> list[dict]:
    return [parse_frontmatter(f) for f in sorted(ISSUES.glob("*-[0-9]*.md"))]


def set_field(path: Path, key: str, value: str) -> None:
    text = read_text(path)
    if re.search(rf"(?m)^{key}:", text):
        text = re.sub(rf"(?m)^{key}:.*$", f"{key}: {value}".rstrip(), text, count=1)
    else:  # insert before closing --- of frontmatter
        text = re.sub(r"(?m)^---$", f"{key}: {value}\n---", text.replace("---", "\x00", 1), count=1)
        text = text.replace("\x00", "---", 1)
    path.write_text(text, encoding="utf-8")


def sort_key(fm: dict):
    return (STATUS_ORDER.get(fm.get("status", ""), 5),
            PRI_ORDER.get(fm.get("priority", "?"), 4),
            fm.get("id", ""))


def open_issues() -> list[dict]:
    return [i for i in all_issues()
            if i.get("status") not in ("done", "dropped") and i.get("id")]


def regen_index() -> None:
    rows = sorted(open_issues(), key=sort_key)
    lines = [
        "# Backlog — open items",
        "",
        "<!-- GENERATED by tools/track.py — do not edit. Regenerate: python tools/track.py index -->",
        "",
        "One line per open issue; full detail in `issues/<id>.md`. Process: [readme.md](readme.md).",
        "Issue frontmatter is the source of truth; this file is a generated view",
        "(sorted by status, then priority). Filtered views: `python tools/track.py list --help`.",
        "",
        "| id | title | type | pri | effort | risk | area | status | sprint |",
        "|----|-------|------|-----|--------|------|------|--------|--------|",
    ]
    for i in rows:
        lines.append(
            f"| [{i['id']}](issues/{i['id']}.md) | {i.get('title','')} | {i.get('type','')} "
            f"| {i.get('priority','?')} | {i.get('effort','?')} | {i.get('risk','?')} "
            f"| {i.get('area','')} | {i.get('status','')} | {i.get('sprint','')} |")
    BACKLOG.write_text("\n".join(lines) + "\n", encoding="utf-8")


# ---- reports -------------------------------------------------------------
