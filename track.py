def _esc(s) -> str:
    return str(s).replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")


def open_grouped() -> tuple[list, list]:
    """Return ([(status, label, [items sorted by priority])], all_open)."""
    opens = open_issues()
    groups = [(st, lbl, sorted((i for i in opens if i.get("status") == st),
                               key=lambda r: (PRI_ORDER.get(r.get("priority", "?"), 4), r.get("id", ""))))
              for st, lbl in OPEN_GROUPS]
    return groups, opens


def done_entries(limit: int = DONE_LIMIT) -> list[str]:
    if not DONE.exists():
        return []
    return [l for l in DONE.read_text(encoding="utf-8").splitlines() if l.startswith("- ")][:limit]


def report_md() -> str:
    groups, opens = open_grouped()
    counts = "  ·  ".join(f"**{lbl.lower()}** {len(items)}" for _, lbl, items in groups)
    out = [f"# WorkQuarry report — {today()}", "",
           f"**Open:** {len(opens)}  ·  {counts}", ""]
    hdr = "| id | title | type | pri | effort | risk | area | sprint |"
    sep = "|----|-------|------|-----|--------|------|------|--------|"
    for _st, label, items in groups:
        out += [f"## {label} ({len(items)})", ""]
        if not items:
            out += ["_none_", ""]
            continue
        out += [hdr, sep]
        for i in items:
            out.append(f"| {i['id']} | {i.get('title','')} | {i.get('type','')} "
                       f"| {i.get('priority','?')} | {i.get('effort','?')} | {i.get('risk','?')} "
                       f"| {i.get('area','')} | {i.get('sprint','')} |")
        out.append("")
    if done := done_entries():
        out += [f"## Recently done ({len(done)})", ""] + done + [""]
    return "\n".join(out) + "\n"


def _rows_html(items) -> str:
    rows = []
    for i in items:
        pri = i.get("priority", "?")
        cls = " class='pri-p1'" if pri == "p1" else ""
        rows.append("<tr>"
                    f"<td class='id'>{_esc(i['id'])}</td>"
                    f"<td>{_esc(i.get('title',''))}</td>"
                    f"<td>{_esc(i.get('type',''))}</td>"
                    f"<td{cls}>{_esc(pri)}</td>"
                    f"<td>{_esc(i.get('effort','?'))}</td>"
                    f"<td>{_esc(i.get('risk','?'))}</td>"
                    f"<td>{_esc(i.get('area',''))}</td>"
                    f"<td>{_esc(i.get('sprint',''))}</td></tr>")
    return "".join(rows)


def report_html() -> str:
    groups, opens = open_grouped()
    chips = [f"<span class='chip'><b>{len(opens)}</b> open</span>"]
    chips += [f"<span class='chip'><b>{len(items)}</b> {_esc(lbl.lower())}</span>" for _, lbl, items in groups]
    cols = ["id", "title", "type", "pri", "effort", "risk", "area", "sprint"]
    thead = "<tr>" + "".join(f"<th>{c}</th>" for c in cols) + "</tr>"
    sections = []
    for _st, label, items in groups:
        body = _rows_html(items) or f"<tr><td colspan='{len(cols)}' class='empty'>none</td></tr>"
        sections.append(f"<h2>{_esc(label)} <span class='ct'>({len(items)})</span></h2>"
                        f"<table><thead>{thead}</thead><tbody>{body}</tbody></table>")
    if done := done_entries():
        lis = []
        for line in done:
            t = re.sub(r"\[([^\]]+)\]\(([^)]+)\)", r"<a href='\2'>\1</a>", _esc(line[2:]))
            lis.append(f"<li>{t}</li>")
        sections.append(f"<h2>Recently done <span class='ct'>({len(done)})</span></h2>"
                        f"<ul class='done'>{''.join(lis)}</ul>")
    return (f"<!doctype html>\n<html lang='en'><head><meta charset='utf-8'>"
            f"<meta name='viewport' content='width=device-width,initial-scale=1'>"
            f"<title>WorkQuarry report — {today()}</title>\n<style>{REPORT_CSS}</style></head>\n"
            f"<body>\n<h1>WorkQuarry report</h1>\n<p class='meta'>Generated {today()}</p>\n"
            f"<div class='chips'>{''.join(chips)}</div>\n{''.join(sections)}\n"
            f"<p class='foot'>Generated by "
            f"<a href='https://github.com/ara3d/workquarry'>WorkQuarry</a>.</p>\n</body></html>\n")


def cmd_report(a) -> None:
    wrote = False
    if a.html:
        Path(a.html).write_text(report_html(), encoding="utf-8")
        print(f"wrote {a.html}")
        wrote = True
    if a.md:
        Path(a.md).write_text(report_md(), encoding="utf-8")
        print(f"wrote {a.md}")
        wrote = True
    if not wrote:  # no output path: Markdown to stdout as UTF-8 so it pipes cleanly on any OS
        sys.stdout.buffer.write(report_md().encode("utf-8"))


def cmd_new(a) -> None:
    if a.area not in AREAS:
        sys.exit(f"unknown area '{a.area}' (expected one of {sorted(AREAS)}; "
                 f"edit areas in {CONFIG_PATH})")
    iid = f"{a.area}-{next_num():03d}"
    links = "[" + ", ".join(s.strip() for s in a.links.split(",") if s.strip()) + "]" if a.links else "[]"
    def q(v): return f'"{v}"' if v == "?" else v
    fm = (f"---\nid: {iid}\ntitle: {a.title}\ntype: {a.type}\nstatus: {a.status}\n"
          f"priority: {q(a.priority)}\neffort: {q(a.effort)}\nrisk: {q(a.risk)}\n"
          f"area: {a.area}\nsprint: {a.sprint}\ncreated: {today()}\nclosed:\nlinks: {links}\n---\n")
    if a.dry_run:
        print(f"would create {iid}\n{fm}")
        return
    path = ISSUES / f"{iid}.md"
    path.write_text(fm, encoding="utf-8")
    regen_index()
    print(f"{iid}\n{path}")


def cmd_set(a) -> None:
    path = ISSUES / f"{a.id}.md"
    if not path.exists():
        sys.exit(f"no such issue: {a.id}")
    changes = {k: v for k, v in
               [("status", a.status), ("priority", a.priority), ("effort", a.effort),
                ("risk", a.risk), ("title", a.title),
                ("sprint", "" if a.sprint == "none" else a.sprint)]
               if v is not None}
    if not changes:
        sys.exit("nothing to set")
    for k, v in changes.items():
        set_field(path, k, f'"{v}"' if v == "?" else v)
    regen_index()
    print(f"{a.id}: " + ", ".join(f"{k}={v or '(cleared)'}" for k, v in changes.items()))


def cmd_close(a) -> None:
    path = ISSUES / f"{a.id}.md"
    if not path.exists():
        sys.exit(f"no such issue: {a.id}")
    status = "dropped" if a.dropped else "done"
    set_field(path, "status", status)
    set_field(path, "closed", today())
    fm = parse_frontmatter(path)
    done = DONE.read_text(encoding="utf-8").splitlines()
    entry = f"- {today()} — [{a.id}](issues/{a.id}.md) — {fm.get('title', a.id)} — {a.outcome or status}"
    for i, line in enumerate(done):
        if line.startswith("- "):
            done.insert(i, entry)
            break
    else:
        done.append(entry)
    DONE.write_text("\n".join(done) + "\n", encoding="utf-8")
    regen_index()
    print(f"{a.id} {status}")


def cmd_list(a) -> None:
    rows = open_issues() if a.open else [i for i in all_issues() if i.get("id")]
    for attr, key in [("status", "status"), ("area", "area"), ("type", "type"),
                      ("priority", "priority"), ("sprint", "sprint")]:
        if (want := getattr(a, attr)) is not None:
            rows = [r for r in rows if r.get(key, "") == want]
    keys = {"priority": lambda r: (PRI_ORDER.get(r.get("priority", "?"), 4), r.get("id", "")),
            "id": lambda r: r.get("id", ""),
            "status": sort_key,
            "sprint": lambda r: (r.get("sprint", "") == "", r.get("sprint", ""), sort_key(r)),
            "created": lambda r: (r.get("created", ""), r.get("id", ""))}
    rows = sorted(rows, key=keys[a.sort])
    cols = ["id", "title", "type", "priority", "effort", "risk", "area", "status", "sprint", "created"]
    if a.format == "csv":
        buf = io.StringIO()
        w = csv.writer(buf, lineterminator="\n")
        w.writerow(cols)
        for r in rows:
            w.writerow([r.get(c, "") for c in cols])
        print(buf.getvalue(), end="")
    else:
        widths = [max(len(c), *(len(r.get(c, "")) for r in rows)) if rows else len(c) for c in cols]
        print("  ".join(c.ljust(w) for c, w in zip(cols, widths)))
        for r in rows:
            print("  ".join(r.get(c, "").ljust(w) for c, w in zip(cols, widths)))


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    sub = p.add_subparsers(dest="cmd", required=True)

    n = sub.add_parser("new")
    n.add_argument("--title", required=True)
    n.add_argument("--area", required=True)
    n.add_argument("--type", default="idea",
                   choices=["feature", "debt", "bug", "idea", "problem", "retire"])
    n.add_argument("--status", default="idea", choices=["idea", "ready", "in-progress"])
    n.add_argument("--priority", default="?", choices=["p1", "p2", "p3", "?"])
    n.add_argument("--effort", default="?", choices=["S", "M", "L", "?"])
    n.add_argument("--risk", default="?", choices=["low", "med", "high", "?"])
    n.add_argument("--sprint", default="")
    n.add_argument("--links", default="")
    n.add_argument("--dry-run", action="store_true")
    n.set_defaults(func=cmd_new)

    s = sub.add_parser("set")
    s.add_argument("id")
    s.add_argument("--status", choices=["idea", "ready", "in-progress"])
    s.add_argument("--priority", choices=["p1", "p2", "p3", "?"])
    s.add_argument("--effort", choices=["S", "M", "L", "?"])
    s.add_argument("--risk", choices=["low", "med", "high", "?"])
    s.add_argument("--sprint", help="sprint name, or 'none' to clear")
    s.add_argument("--title")
    s.set_defaults(func=cmd_set)

    l = sub.add_parser("list")
    l.add_argument("--status")
    l.add_argument("--area")
    l.add_argument("--type")
    l.add_argument("--priority")
    l.add_argument("--sprint")
    l.add_argument("--open", action="store_true", help="open items only")
    l.add_argument("--sort", default="status", choices=["priority", "id", "status", "sprint", "created"])
    l.add_argument("--format", default="table", choices=["table", "csv"])
    l.set_defaults(func=cmd_list)

    c = sub.add_parser("close")
    c.add_argument("id")
    c.add_argument("--outcome", default="")
    c.add_argument("--dropped", action="store_true")
    c.set_defaults(func=cmd_close)

    r = sub.add_parser("report", help="render a shareable HTML and/or Markdown snapshot")
    r.add_argument("--html", help="write a standalone HTML report to this path")
    r.add_argument("--md", help="write a Markdown report to this path")
    r.set_defaults(func=cmd_report)

    sub.add_parser("index").set_defaults(func=lambda a: regen_index())
    sub.add_parser("next-id").set_defaults(func=lambda a: print(next_num()))

    a = p.parse_args()
    a.func(a)


if __name__ == "__main__":
    main()
